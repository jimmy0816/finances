export type CalcLocale = 'zh-TW' | 'en' | 'ja';

/* ── Unit formatting helpers ── */
export function fmtWanLocale(n: number, locale: CalcLocale): string {
  const wan = n / 10000;
  if (locale === 'zh-TW') {
    if (wan >= 10000) return `${(wan / 10000).toFixed(1)} 億`;
    return `${wan.toFixed(1)} 萬`;
  }
  if (locale === 'ja') {
    if (wan >= 10000) return `${(wan / 10000).toFixed(1)} 億`;
    if (wan >= 1) return `${wan.toFixed(1)} 万`;
    return new Intl.NumberFormat('ja-JP', { maximumFractionDigits: 0 }).format(n);
  }
  // en: full number
  if (n >= 100000000) return `NT$ ${(n / 100000000).toFixed(1)}B`;
  if (n >= 10000000) return `NT$ ${(n / 10000000).toFixed(1)}0M`;
  return `NT$ ${new Intl.NumberFormat('en-US', { maximumFractionDigits: 0 }).format(n)}`;
}

export function fmtNumLocale(n: number, locale: CalcLocale): string {
  const localeStr = locale === 'zh-TW' ? 'zh-TW' : locale === 'ja' ? 'ja-JP' : 'en-US';
  return new Intl.NumberFormat(localeStr, { maximumFractionDigits: 0 }).format(n);
}

